class Warehouse:
    @staticmethod
    def get(address):
        entity = Entity()
        return entity


class Entity:
    @staticmethod
    def force_data_by_name(name):
        variable = Variable()
        return variable


class Variable:
    @staticmethod
    def force_series(name):
        times_series = TimeSeries()
        return times_series


class TimeSeries:
    @staticmethod
    def get():
        return None

    @staticmethod
    def set_data(start_point, values):
        start_point = start_point
        values = values